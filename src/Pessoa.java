public class Pessoa {

    String nome;
    String cpf;
    String rg;
    String telefone;
    String telContato;
    String endereco;
    String assinatura;

    Pessoa (String _nome, String _cpf, String _rg, String _telefone, String _telContato, String _endereco, String _assinatura) {
        this.nome = _nome;
        this.cpf = _cpf;
        this.rg = _rg;
        this.telefone = _telefone;
        this.telContato = _telContato;
        this.endereco = _endereco;
        this.assinatura = _assinatura;
    }
}
